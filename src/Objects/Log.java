package Objects;

public class Log {
    //Pizza pizza;
    String name;
    int count;
    public Log(String name, int count){

        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getCount() {
        return count;
    }
    public void setCount(int count) {
        this.count = count;
    }


}
